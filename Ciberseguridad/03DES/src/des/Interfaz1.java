    private void mostrarContenidoArchivo(String ruta) throws IOException {
        
        if (ruta == null || ruta.isEmpty()) {
            mostrarMensaje("‚ö†Ô∏è Por favor selecciona un archivo.");
            return;
        }

        File archivo = new File(ruta);
        if (!archivo.exists()) {
            mostrarMensaje("‚ö†Ô∏è El archivo no existe.");
            return;
        }

        // Crear y mostrar el di√°logo
        new Dialog_Leer(this, ruta).setVisible(true);
    
    }

    public void mostrarMensaje(String mensaje) {
        Txt_Estado.append(mensaje + "\n");
    }
    
