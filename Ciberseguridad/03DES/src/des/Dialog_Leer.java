    private static String ruta;
    public Dialog_Leer(java.awt.Frame parent, String ruta) {
        super(parent, "üìñ Contenido de: " + new File(ruta).getName(), true);
        this.ruta = ruta;
        initComponents();
        
        // Cargar y mostrar contenido del archivo
        try {
            byte[] bytes = Files.readAllBytes(Paths.get(ruta));
            String extension = new File(ruta).getName();

            // Establecer contenido inicial seg√∫n tipo de archivo
            if (extension.endsWith(".cifrado")) {
                Cmb_Formato.setSelectedItem("Hexadecimal");
                Txt_Area.setText(bytesToHex(bytes));
            } else {
                Cmb_Formato.setSelectedItem("Texto");
                Txt_Area.setText(new String(bytes));
            }

            // Cambiar formato al seleccionar una opci√≥n
            Cmb_Formato.addActionListener(e -> {
                String seleccion = (String) Cmb_Formato.getSelectedItem();
                if ("Hexadecimal".equals(seleccion)) {
                    Txt_Area.setText(bytesToHex(bytes));
                } else if ("Base64".equals(seleccion)) {
                    Txt_Area.setText(Base64.getEncoder().encodeToString(bytes));
                } else {
                    Txt_Area.setText(new String(bytes)); // texto plano
                }
            });

        } catch (IOException ex) {
            Txt_Area.setText("‚ùå Error al leer el archivo: " + ex.getMessage());
        }
    }

